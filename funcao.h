#include <stdio.h>
#include <stdlib.h>


float soma(float soma1, float soma2){
	float totalSoma;
	
	totalSoma= soma1 + soma2;
	return totalSoma;
}

float subtracao(float sub1, float sub2){
	float totalSubtracao;

	totalSubtracao= sub1 - sub2;
	return totalSubtracao;
}

float multiplicacao(float mult1, float mult2){
	float totalMultiplicacao;
	
	totalMultiplicacao= mult1 * mult2;
	return totalMultiplicacao;
}

float divisao(float div1, float div2){
	float totalDivisao;
	
	totalDivisao= div1/div2;
	return totalDivisao;
}

